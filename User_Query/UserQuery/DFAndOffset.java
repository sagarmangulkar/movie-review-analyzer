
public class DFAndOffset{
    public DFAndOffset(int df, int offset) {
        this.df = df;
        this.offset = offset;
    }

    public int getDf() {
        return df;
    }

    public void setDf(int df) {
        this.df = df;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    private int df;
    private int offset;
}